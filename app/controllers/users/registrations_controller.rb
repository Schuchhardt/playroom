  def create
    # Build resource using devise parameter sanitizer
    build_resource(sign_up_params)

    # Attempt to find the establishment
    establishment = Establishment.find_by(rbd: sign_up_params[:rbd].gsub(/-|\./, '').strip)

    if establishment.nil?
      # If no establishment, add an error to the base object and render the form again
      resource.errors.add(:base, "No se encontró un colegio con el RBD proporcionado. Por favor, contacta a tu colegio para obtener más información.")
      respond_with_navigational(resource) { render :new }
    else
      # If establishment is found, proceed with user creation
      resource.user_type = 'teacher'
      resource.save
      yield resource if block_given?
      if resource.persisted?
        UserEstablishment.create(user: resource, establishment: establishment)
        if resource.active_for_authentication?
          set_flash_message! :notice, :signed_up
          sign_up(resource_name, resource)
          respond_with resource, location: after_sign_up_path_for(resource)
        else
          set_flash_message! :notice, :"signed_up_but_#{resource.inactive_message}"
          expire_data_after_sign_in!
          respond_with resource, location: after_inactive_sign_up_path_for(resource)
        end
      else
        clean_up_passwords resource
        set_minimum_password_length
        respond_with resource
      end
    end
  end

